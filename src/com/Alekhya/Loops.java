package com.Alekhya;

public class Loops {
    public static void main(String[] args) {
        for(int i=0; i<=5; i++) {
            System.out.println(i);
        }
        int num=1;
        while(num !=5) {
            System.out.println(num);
            num++;
        }
        int n=1;
        do{
            System.out.println("Hello world");
        }while(n != 1);
    }
}
